                                   //mas comportamento distintos, especializados para cada classe derivada, usando uma referência a um objeto do tipo da superclasse

//Com polimorfimso podemos:
//-Invocar métodos da calsse derivada através da classe base em tempo de execução
//-Permitir que classes forneçam diferentes implementações de métodos que são chamados com o mesmo nome

//3 tipos de polimorfismo em C#:

//Polimorfismo com herança,  classes derivadas sobrescrevendo os membros da classe base (virtual e override)

//Polimorfismo com interface, classes que implementam a interface com comportamentos diferentes, métodos com mesmo nome nas classes mas funcionalidades diferentes

//Polimorfismo com classe abstrata, as classes derivadas incluem os detalhes de implementação nos métodos abstratos







/*

1.O que é uma classe e qual a sua composição?
Classe: É um modelo ou blueprint que define atributos (campos) e comportamentos (métodos) de objetos.

Composição: Campos, propriedades, métodos, construtores e eventos.

2. O que é um objeto e como pode ser criado?
Objeto: É uma instância de uma classe.

Criação: Usando a palavra-chave new(ex: MinhaClasse obj = new MinhaClasse();).

3.Qual a diferença entre classe e objeto?
Classe: Modelo ou template.

Objeto: Instância concreta criada a partir da classe.

4. O que é um método e como pode ser criado e chamado?
Método: Bloco de código que executa uma ação.

Criação: Dentro de uma classe, com retorno, nome e parâmetros (ex: void Metodo() { }).

Chamada: Usando o nome do método (ex: obj.Metodo();).

5.O que é um parâmetro de método?
Parâmetro: Variável definida na assinatura do método para receber valores.

6. O que é um argumento?
Argumento: Valor passado para um parâmetro ao chamar um método.

7. O que é um construtor e qual o seu objetivo?
Construtor: Método especial que inicializa um objeto.

Objetivo: Configurar o estado inicial do objeto.

8. Quais os usos da palavra-chave this?
Referenciar membros da classe atual.

Diferenciar parâmetros de campos com o mesmo nome.

Chamar outro construtor na mesma classe.

9. O que é um método com retorno?
Método com retorno: Retorna um valor após sua execução (ex: int Soma(int a, int b) { return a + b; }).

10.O que é sobrecarga de método?
Sobrecarga: Criar múltiplos métodos com o mesmo nome, mas com parâmetros diferentes.

11. Como é feita a passagem de parâmetros por valor?
Passagem por valor: Uma cópia do valor é passada para o método (não afeta a variável original).

12. Como é feita a passagem de parâmetros por referência?
Passagem por referência: Usando ref ou out. O método trabalha diretamente com a variável original.

13. Qual a diferença entre os parâmetros out e ref?
ref: A variável deve ser inicializada antes de ser passada.

out: A variável não precisa ser inicializada antes de ser passada.

14. O que são argumentos nomeados?
Argumentos nomeados: Passar argumentos especificando o nome do parâmetro (ex: Metodo(nome: "João", idade: 25);).

15.O que são parâmetros opcionais?
Parâmetros opcionais: Parâmetros com valores padrão que podem ser omitidos na chamada.

16. Qual o efeito da palavra-chave static?
static: Torna o membro ou tipo pertencente à classe, não a instâncias.

17. O que é método estático e qual o seu comportamento?
Método estático: Pertence à classe, não a instâncias. Pode ser chamado sem criar um objeto.

18. O que é um campo estático e qual o seu comportamento?
Campo estático: Pertence à classe, compartilhado por todas as instâncias.

19. O que são propriedades?
Propriedades: Membros que encapsulam campos, permitindo leitura e escrita controlada.

20. Qual a diferença entre campos e propriedades?
Campos: Variáveis que armazenam dados.

Propriedades: Métodos que controlam acesso a campos.

21. O que é uma struct?
struct: Tipo de valor que encapsula dados e comportamentos, semelhante a uma classe, mas com semântica de valor.

22. Qual a diferença entre uma struct e uma classe?
struct: Tipo de valor, alocado na stack, não suporta herança.

Classe: Tipo de referência, alocado na heap, suporta herança.

23. O que é uma Enum?
Enum: Tipo que define um conjunto de constantes nomeadas.

24. Quais são e qual o escopo dos modificadores de acesso?
Modificadores: public, private, protected, internal, protected internal, private protected.

Escopo: Controlam onde membros ou tipos podem ser acessados.

25. Qual a regra geral para usar os modificadores de acesso?
Regra geral: Use o modificador mais restritivo possível para garantir encapsulamento e segurança.

*/

